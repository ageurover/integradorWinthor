    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        String senha = new String(edtSenha.getPassword());
        String licencaCnpj = "";

        if (!Main.licenca.isEmpty()) {
            if (!Main.cnpjMatriz.isEmpty()) {
                licencaCnpj = Formato.geraChaveLicenca(Main.cnpjMatriz);
                if (!licencaCnpj.equalsIgnoreCase(Main.licenca)) {
                    MessageDialog.error("Licença de uso invalida para o Cnpj informado! \nAcesse as configurações e cadastre a licença de uso correta!");
                    return;
                }
            } else {
                MessageDialog.error("Cnpj da Matriz não informado! \nAcesse as configurações e cadastre o Cnpj da Matriz!");
                return;
            }
        } else {
            MessageDialog.error("Licença de uso não informada! \nAcesse as configurações e cadastre a licença de uso!");
            return;
        }

        if ((!edtUsuario.getText().isEmpty()) && (!senha.isEmpty())) {
            if (autorizaLogin(edtUsuario.getText(), senha)) {
                Main.Usuario = edtUsuario.getText();
                Main.dialogLogin.setVisible(false);
                Main.dialogMenu = new Brz000();
                Main.dialogMenu.setVisible(true);
            } else {
                MessageDialog.error("Acesso não autorizado!");
            }
        } else {
            MessageDialog.error("Informe o usuario e a senha por favor!");
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

