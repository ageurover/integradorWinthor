    private boolean autorizaLogin(String usuario, String senha) {
        IntegracaoWinthorDb wint = new IntegracaoWinthorDb();
        String strSelect = "Select MATRICULA, NOME, SITUACAO from pcempr "
                + " where SITUACAO = 'A'"
                + " and UPPER(nome_guerra) = UPPER('" + usuario + "') "
                + " and UPPER(decrypt(SENHABD,USUARIOBD)) = UPPER('" + senha + "') "
                + " and matricula in ( SELECT PCCONTRO.codusuario FROM PCCONTRO, PCROTINA "
                + " WHERE PCROTINA.CODMODULO IN  (98) "
                + "  AND PCROTINA.codigo = PCCONTRO.codrotina "
                + "  AND PCCONTRO.ACESSO = 'S')";
        try {
            wint.openConectOracle();
            List lst = wint.selectDados(strSelect);
            return (!lst.isEmpty());

        } catch (Exception ex) {
            trataErro.trataException(ex);
            return false;
        }
    }

