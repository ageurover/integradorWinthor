    private void btnPesquisaPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaPedidoActionPerformed
        try {
            if ((!edtNumeroNota.getText().isEmpty()) && (!edtCodFornec.getText().isEmpty()) && (!edtCodFilial.getText().isEmpty())) {
                buscaLancamentoWinthor(edtCodFornec.getText(), edtNumeroNota.getText(), edtCodFilial.getText());
                btnPesquisaPedido.setEnabled(false);
                edtNumeroNota.setEditable(false);
                edtCodFilial.setEditable(false);
                btnVinculaTitulo.setEnabled(true);
                edtCodContaContabil.setEditable(true);
            } else {
                trataErro.lstErros.clear();
                trataErro.addListaErros("ATENÇÃO: Deve ser informado o numero da Nota, o codigo do Fornecedor e o codigo da filial!");
                trataErro.mostraListaErros();
                trataErro.lstErros.clear();
                btnLimparActionPerformed(evt);
            }
        } catch (Exception ex) {
            trataErro.trataException(ex);
        }
    }//GEN-LAST:event_btnPesquisaPedidoActionPerformed

