    private void buscaLancamentoWinthor(String codFornec, String numNota, String codFilial) {
        IntegracaoWinthorDb wint = new IntegracaoWinthorDb();

        try {
            String strSelect = "SELECT PCLANC.codfilial, PCLANC.recnum,PCLANC.numnota, PCLANC.dtlanc, PCLANC.codfornec, PCFORNEC.fornecedor,"
                    + " PCLANC.valor, PCLANC.dtvenc, PCLANC.historico "
                    + " FROM PCLANC, PCFORNEC "
                    + " WHERE PCLANC.codfornec = PCFORNEC.codfornec"
                    + " AND PCLANC.NUMNOTA = " + numNota
                    + " AND PCLANC.CODFORNEC = " + codFornec
                    + " AND PCLANC.CODFILIAL = " + codFilial;

            wint.openConectOracle();
            List lst = wint.selectDados(strSelect);

            edtDetalhePedido.setText("");
            edtDetalhePedido.append("Lançamentos Encontrados no Winthor\n");

            for (Object object : lst.toArray()) {
                edtDetalhePedido.append(lst.size() + "\n");
                edtDetalhePedido.append(object.toString() + "\n");
            }
            if (lst.isEmpty()) {
                edtDetalhePedido.append("O Lançamento não foi localizado\n");
            }
            lst = null;
            wint.closeConectOracle();

        } catch (Exception ex) {
            trataErro.trataException(ex);
        }

    }

    private void baixaLancamento(String numNota, String codFornec, String codFilial, String numConta) throws Exception {
        IntegracaoWinthorDb wint = new IntegracaoWinthorDb();

        int result = 0;
        try {
            wint.openConectOracle();
            // passo 1 - Alterar os dados do lançamento fazendo a baixa no sistema
            String updString = "UPDATE PCLANC SET VPAGO = VALOR, DTPAGTO = DTVENC, CODCONTA = " + numConta
                    + " WHERE CODFORNEC = " + codFornec
                    + " AND NUMNOTA = " + numNota
                    + " AND CODFILIAL = " + codFilial;

            result = wint.updateDados(updString);
            if (result > 0) {
                edtDetalhePedido.append("[" + result + "] Titulos Alterados: " + result + "\n");
                edtDetalhePedido.append("Os lançamentos foram atualizado na base de dados do winthor\n");
                wint.closeConectOracle();
            }
        } catch (Exception ex) {
            trataErro.trataException(ex);
            throw ex;
        }

    }

